package ntu.nlp.component;

public class Comment {

	private int serial;
	
	private int rank;
	
	private String msg;
	
//-----------------------------[getter and setter]--------------------------------------------
	
	public int getSerial() {
		return serial;
	}

	public void setSerial(int serial) {
		this.serial = serial;
	}

	public int getRank() {
		return rank;
	}

	public void setRank(int rank) {
		this.rank = rank;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}	
	
	
}
